    if not args.git:
        print("  2. (Recommended) Re-run with --git for hard quality enforcement via git hooks")
    if not args.claude_hooks:
        print("  3. (Recommended) Re-run with --claude-hooks for Claude Code session hooks")
    print("  4. Run: python scripts/run.py check_status.py")


