#!/usr/bin/env python3
"""Initialize a project directory with the Harness Workflow structure.

Creates .harness/ state, OpenSpec directory structure, and copies templates.
Delegates to OpenSpec CLI when available.

Usage:
    python run.py init_harness.py . --feature add-auth
    python run.py init_harness.py /path/to/project --force
"""

import argparse
import json
import shlex
import shutil
import sys
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Dict, Optional

SCRIPTS_DIR = Path(__file__).parent
sys.path.insert(0, str(SCRIPTS_DIR))

from harness_shared import (
    detect_build_commands,
    detect_language,
    detect_package_manager,
    detect_framework,
    detect_openspec_cli,
    run_command,
    save_state,
)

SKILL_DIR = SCRIPTS_DIR.parent
TEMPLATES_DIR = SKILL_DIR / "templates"
CONFIG_DIR = SKILL_DIR / "config"
CICD_TEMPLATES_DIR = TEMPLATES_DIR / "cicd"

OPENSPEC_CONFIG_YAML = """\
schema: "1.0"
tools:
  - claude
context:
  description: "Auto-generated by harness-workflow init"
"""


def create_harness_dir(
    project_dir: Path,
    language: str,
    framework: Optional[str],
    package_manager: Optional[str],
    force: bool = False,
) -> Dict[str, Any]:
    """Create .harness/ directory with state.json and config.yaml."""
    harness_dir = project_dir / ".harness"

    if harness_dir.exists() and not force:
        state_file = harness_dir / "state.json"
        if state_file.exists():
            return {"existed": True, "path": str(harness_dir)}

    harness_dir.mkdir(parents=True, exist_ok=True)

    state = {
        "version": "2.0.0",
        "created_at": datetime.now(timezone.utc).isoformat(),
        "updated_at": datetime.now(timezone.utc).isoformat(),
        "language": language,
        "framework": framework,
        "package_manager": package_manager,
        "stages": {
            "intent": "pending",
            "spec": "pending",
            "plan": "pending",
            "harness": "pending",
            "execute": "pending",
        },
        "current_stage": "intent",
    }
    save_state(project_dir, state)

    config_src = CONFIG_DIR / "harness-config.yaml"
    config_dst = harness_dir / "config.yaml"
    if config_src.exists() and (not config_dst.exists() or force):
        shutil.copy2(config_src, config_dst)

    return {"existed": False, "path": str(harness_dir)}


def create_openspec_structure(
    project_dir: Path,
    feature_name: str,
    force: bool = False,
) -> Dict[str, Any]:
    """Create OpenSpec directory structure with per-feature subdirectories.

    Correct structure:
        openspec/
          config.yaml
          changes/
            <feature>/
              .openspec.yaml
              proposal.md
              design.md
              tasks.md
    """
    openspec_dir = project_dir / "openspec"
    changes_dir = openspec_dir / "changes" / feature_name
    created = []

    # Try OpenSpec CLI first
    if detect_openspec_cli():
        print("  OpenSpec CLI detected, delegating initialization...")
        safe_dir = shlex.quote(str(project_dir))
        result = run_command(
            f"openspec init {safe_dir} --tools claude --force",
            project_dir,
            timeout=30,
        )
        if result["success"]:
            print("  Initialized via OpenSpec CLI")
            changes_dir.mkdir(parents=True, exist_ok=True)
            return {"method": "cli", "created": created}
        else:
            print(f"  OpenSpec CLI failed: {result['stderr'].strip()}")
            print("  Falling back to manual structure")

    # Manual structure
    openspec_dir.mkdir(parents=True, exist_ok=True)
    created.append(str(openspec_dir))

    config_path = openspec_dir / "config.yaml"
    if not config_path.exists() or force:
        config_path.write_text(OPENSPEC_CONFIG_YAML, encoding="utf-8")
        created.append(str(config_path))

    changes_dir.mkdir(parents=True, exist_ok=True)
    created.append(str(changes_dir))

    metadata_path = changes_dir / ".openspec.yaml"
    if not metadata_path.exists() or force:
        metadata = {
            "name": feature_name,
            "created": datetime.now(timezone.utc).isoformat(),
            "status": "draft",
            "tools": ["claude"],
        }
        metadata_path.write_text(
            json.dumps(metadata, indent=2) + "\n",
            encoding="utf-8",
        )
        created.append(str(metadata_path))

    template_mapping = {
        "proposal.md": "proposal.md",
        "design.md": "design.md",
        "tasks.md": "tasks.md",
    }
    for template_name, dest_name in template_mapping.items():
        src = TEMPLATES_DIR / template_name
        dst = changes_dir / dest_name
        if src.exists() and (not dst.exists() or force):
            shutil.copy2(src, dst)
            created.append(str(dst))

    return {"method": "manual", "created": created}


def create_intent_file(project_dir: Path, force: bool = False) -> Dict[str, Any]:
    """Copy intent.md template to project root."""
    src = TEMPLATES_DIR / "intent.md"
    dst = project_dir / "intent.md"

    if not src.exists():
        return {"copied": False, "reason": "Template not found"}

    if dst.exists() and not force:
        return {"copied": False, "reason": "Already exists (use --force to overwrite)"}

    shutil.copy2(src, dst)
    return {"copied": True, "path": str(dst)}


